/* Arrays: Son una caja o matriz ordenada que contiene a varios elementos. Estos elementos pueden ser de cualquier tipo, e incluso se pueden mezclar todos los tipos de elmentos.
           Para definirla se coloca la palabra reservada "var" luego el nombre del Array y seguido del signo "=" se coloca entre corchetes los elementos separados entre ";".

           var Esto_es_un_Array = [elemento_1; elemento_2; etc];


           Ademas existen distintas propiedades para los arrays, como:

           .length : Que nos devuelve la cantidad de elementos que contiene el Array.

           Y algunos metodos, como:

           .push(nombre_del_elemento)  : que introduce un elemento al final del array.
           .pop()   : nos devuelve el elemento en la posicion que le asignamos, pero al mismo tiempo lo elimina del Array.

           .unshift(nombre_del_elemento) : introduce el nuevo elemento al inicio del array.
           .shift() : elimina el primer elmento del array.

*/
